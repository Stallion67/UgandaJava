public class ForLoops {

    public  static  void  main (String args[]){

        for (int i=10; i>1; i--){
            System.out.println(i);
        }


        //For Loop Iterate an array
        int arr[]={2,11,45,9};

          //i starts with 0 as arry index starts with 0 too
        for (int i=0; i<arr.length; i++){
            System.out.println(arr[i]);
        }
    }
}
